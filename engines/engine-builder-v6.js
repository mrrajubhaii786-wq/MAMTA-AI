/**
 * MAMTA AI V6 — Builder Engine
 * AI-powered project generation. Real code, real files, real projects.
 * @version 6.0.0
 */
class BuilderEngineV6 extends BaseEngine {
  constructor() {
    super('BuilderEngineV6', '6.0.0');
    this.supabase = null;
    this.aiService = null;
    this.generatedProjects = [];
  }

  async init() {
    await super.init();
    if (typeof SupabaseService !== 'undefined') {
      this.supabase = new SupabaseService();
      await this.supabase.init();
    }
    this.log('🔨 Builder V6 ready');
    return true;
  }

  enableAI(aiService) {
    this.aiService = aiService;
    this.log('🤖 AI Builder enabled');
  }

  async generateProject(name, type = 'webapp', description = '') {
    this.log(`🔨 Generating project: ${name} (${type})`);

    let files = [];
    let architecture = null;
    let dependencies = [];

    // AI-powered generation
    if (this.aiService && this.aiService.hasKey() && description) {
      try {
        this.log('🤖 Requesting AI project generation...');
        const aiResult = await this.aiService.generateProjectFiles(name, type, description);
        if (aiResult && aiResult.length > 0) {
          files = aiResult;
          this.log(`✅ AI generated ${files.length} files`);
        }
      } catch (e) {
        this.error(e);
        this.log('⚠️ AI generation failed, using templates');
      }
    }

    // Template fallback
    if (files.length === 0) {
      files = this.generateTemplateFiles(name, type);
    }

    // AI architecture suggestion
    if (this.aiService && this.aiService.hasKey()) {
      try {
        const arch = await this.aiService.suggestArchitecture(description || name, []);
        architecture = arch;
      } catch (e) {
        architecture = this.fallbackArchitecture(type);
      }
    } else {
      architecture = this.fallbackArchitecture(type);
    }

    const project = {
      id: `proj_${Date.now()}`,
      name,
      type,
      description,
      createdAt: new Date().toISOString(),
      files,
      architecture,
      dependencies,
      aiGenerated: !!(this.aiService && this.aiService.hasKey() && files.length > 0)
    };

    // Save to Supabase
    if (this.supabase) {
      try {
        await this.supabase.insert('memory', {
          type: 'project',
          content: { name, type, description, files: files.map(f => ({ path: f.path, type: f.type })) },
          summary: `Project: ${name} (${type}) - ${files.length} files`,
          metadata: { ai_generated: project.aiGenerated, file_count: files.length }
        });

        await this.supabase.insert('versions', {
          version: `v1.0.0-${Date.now()}`,
          name: `${name} Initial`,
          changes: ['Initial project generation'],
          files: { files },
          build_status: 'success',
          metadata: { project_name: name, type }
        });

        // Add to project graph
        for (const file of files) {
          await this.supabase.insert('project_graph', {
            project_id: project.id,
            node_type: this.detectNodeType(file.path),
            node_name: file.path,
            node_path: file.path,
            dependencies: this.extractDeps(file.content || ''),
            metadata: { ai_generated: project.aiGenerated }
          });
        }

        this.log(`✅ Project stored in Supabase: ${name}`);
      } catch (e) {
        this.error(e);
      }
    }

    this.generatedProjects.push(project);
    this.log(`✅ Project generated: ${files.length} files`);
    return project;
  }

  generateTemplateFiles(name, type) {
    const files = [];

    if (type === 'webapp') {
      files.push(
        { path: 'index.html', type: 'html', content: `<!DOCTYPE html>\n<html lang="en">\n<head>\n  <meta charset="UTF-8">\n  <title>${name}</title>\n  <link rel="stylesheet" href="css/styles.css">\n</head>\n<body>\n  <div id="app"></div>\n  <script src="js/app.js"></script>\n</body>\n</html>` },
        { path: 'css/styles.css', type: 'css', content: `/* ${name} Styles */\n:root { --primary: #6366f1; --dark: #0f172a; --light: #f8fafc; }\nbody { font-family: system-ui; background: var(--dark); color: var(--light); }` },
        { path: 'js/app.js', type: 'js', content: `/**\n * ${name} - Main Application\n * @generated_by MAMTA AI V6\n */\n\nclass App {\n  constructor() { this.initialized = false; }\n  async init() { this.initialized = true; console.log('${name} ready'); }\n}\n\nconst app = new App();\napp.init();` },
        { path: 'js/components/Header.js', type: 'component', content: `class Header { constructor(c) { this.c = c; } render() { this.c.innerHTML = '<header>${name}</header>'; } }` },
        { path: 'assets/README.md', type: 'md', content: `# ${name}\n\nGenerated by MAMTA AI V6.0.0\n\n## Features\n- Modular architecture\n- Component-based UI\n- Supabase integration ready` }
      );
    } else if (type === 'api') {
      files.push(
        { path: 'package.json', type: 'json', content: JSON.stringify({ name, version: '1.0.0', main: 'server.js' }, null, 2) },
        { path: 'server.js', type: 'js', content: `const express = require('express');\nconst app = express();\napp.listen(3000, () => console.log('${name} API running'));` },
        { path: 'routes/index.js', type: 'js', content: `const router = require('express').Router();\nmodule.exports = router;` }
      );
    }

    return files;
  }

  detectNodeType(path) {
    if (path.endsWith('.html')) return 'file';
    if (path.endsWith('.css')) return 'file';
    if (path.endsWith('.js') && path.includes('component')) return 'component';
    if (path.endsWith('.js')) return 'module';
    if (path.endsWith('.sql')) return 'database';
    return 'file';
  }

  extractDeps(content) {
    const deps = [];
    const regex = /import\s+.*?\s+from\s+['"]([^'"]+)['"]/g;
    let match;
    while ((match = regex.exec(content)) !== null) {
      deps.push(match[1]);
    }
    return deps;
  }

  fallbackArchitecture(type) {
    return {
      pattern: 'MVC + Service Layer',
      frontend: type === 'webapp' ? 'Vanilla JS + Modular' : 'N/A',
      backend: 'Supabase',
      database: 'PostgreSQL',
      hosting: 'GitHub Pages + Supabase',
      security: 'RLS + AES-256'
    };
  }

  async analyzeRequirements(description) {
    if (this.aiService && this.aiService.hasKey()) {
      try {
        return await this.aiService.analyzeRequirements(description);
      } catch (e) {
        this.error(e);
      }
    }
    return this.fallbackRequirements(description);
  }

  fallbackRequirements(description) {
    const desc = description.toLowerCase();
    const files = [];
    if (desc.includes('auth')) files.push({ name: 'auth.js', type: 'js', reason: 'Authentication', priority: 'high' });
    if (desc.includes('database') || desc.includes('storage')) files.push({ name: 'db.js', type: 'js', reason: 'Database', priority: 'high' });
    if (desc.includes('api')) files.push({ name: 'api.js', type: 'js', reason: 'API', priority: 'high' });
    if (desc.includes('chat')) files.push({ name: 'chat.js', type: 'js', reason: 'Chat', priority: 'high' });
    if (files.length === 0) files.push({ name: 'app.js', type: 'js', reason: 'Main app', priority: 'high' });
    return files;
  }

  async process(task, context) {
    if (task.action === 'generateProject') {
      return await this.generateProject(task.name, task.type, task.description);
    }
    if (task.action === 'analyze') {
      return await this.analyzeRequirements(task.description);
    }
    return { status: 'unknown_action' };
  }
}

if (typeof module !== 'undefined' && module.exports) {
  module.exports = { BuilderEngineV6 };
}
