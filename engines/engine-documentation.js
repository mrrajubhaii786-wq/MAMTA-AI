/**
 * MAMTA AI V6 — Documentation Engine
 * Auto-generates and updates documentation, changelogs, architecture diagrams
 * @version 6.0.0
 */
class DocumentationEngine extends BaseEngine {
  constructor() {
    super('DocumentationEngine', '6.0.0');
    this.docs = new Map();
    this.supabase = null;
    this.aiService = null;
  }

  async init() {
    await super.init();
    if (typeof SupabaseService !== 'undefined') {
      this.supabase = new SupabaseService();
      await this.supabase.init();
    }
    this.log('📝 Documentation Engine ready');
    return true;
  }

  enableAI(aiService) {
    this.aiService = aiService;
    this.log('🤖 AI Documentation generation enabled');
  }

  // Auto-generate docs from code
  async generateDocs(code, filename, projectName = 'MAMTA AI') {
    this.log(`📝 Generating docs for ${filename}...`);

    let doc = null;

    if (this.aiService && this.aiService.hasKey()) {
      try {
        doc = await this.aiService.generateDocumentation(
          `${projectName} - ${filename}`,
          code,
          'markdown'
        );
        this.log('🤖 AI-generated documentation created');
      } catch (e) {
        this.error(e);
      }
    }

    if (!doc) {
      doc = this.generateTemplateDocs(code, filename, projectName);
    }

    const docEntry = {
      id: `doc_${Date.now()}`,
      filename,
      content: doc,
      generatedAt: new Date().toISOString(),
      aiGenerated: !!(this.aiService && this.aiService.hasKey())
    };

    this.docs.set(filename, docEntry);

    if (this.supabase) {
      try {
        await this.supabase.insert('memory', {
          type: 'knowledge',
          content: { filename, doc },
          summary: `Documentation for ${filename}`,
          metadata: { ai_generated: docEntry.aiGenerated, engine: 'DocumentationEngine' }
        });
      } catch (e) {
        this.error(e);
      }
    }

    return docEntry;
  }

  generateTemplateDocs(code, filename, projectName) {
    const isClass = code.includes('class ');
    const functions = code.match(/(?:async\s+)?function\s+(\w+)/g) || [];
    const classes = code.match(/class\s+(\w+)/g) || [];
    const jsdoc = code.match(/\/\*\*[\s\S]*?\*\//g) || [];

    let doc = `# ${filename}\n\n`;
    doc += `**Project:** ${projectName}\n`;
    doc += `**Generated:** ${new Date().toISOString()}\n`;
    doc += `**Engine:** MAMTA AI Documentation Engine V6\n\n`;

    if (isClass) {
      doc += `## Classes\n\n`;
      classes.forEach(c => {
        const name = c.replace('class ', '');
        doc += `### ${name}\n\n`;
        doc += `Description: ${name} class.\n\n`;
      });
    }

    if (functions.length > 0) {
      doc += `## Functions\n\n`;
      functions.forEach(f => {
        const name = f.replace(/(?:async\s+)?function\s+/, '');
        doc += `- **${name}()**\n`;
      });
      doc += `\n`;
    }

    doc += `## JSDoc Comments\n\n`;
    doc += `Found ${jsdoc.length} documentation blocks.\n\n`;

    doc += `---\n`;
    doc += `*Auto-generated by MAMTA AI V6 Documentation Engine*\n`;

    return doc;
  }

  // Generate changelog from commits/changes
  async generateChangelog(changes, version) {
    let changelog = `# Changelog - ${version}\n\n`;

    if (this.aiService && this.aiService.hasKey()) {
      try {
        const systemPrompt = `You are MAMTA AI Documentation V6. Generate a professional changelog.
Return markdown only.`;
        const result = await this.aiService.callAI(systemPrompt, `Changes: ${JSON.stringify(changes)}\nVersion: ${version}`);
        changelog = result || changelog;
      } catch (e) {
        this.error(e);
      }
    }

    if (!changelog.includes(version)) {
      changelog += `## ${version} - ${new Date().toISOString().split('T')[0]}\n\n`;
      changes.forEach(change => {
        changelog += `- ${change}\n`;
      });
    }

    if (this.supabase) {
      try {
        await this.supabase.insert('memory', {
          type: 'knowledge',
          content: { version, changes, changelog },
          summary: `Changelog for ${version}`,
          metadata: { engine: 'DocumentationEngine' }
        });
      } catch (e) {
        this.error(e);
      }
    }

    return changelog;
  }

  // Generate architecture diagram (Mermaid format)
  async generateArchitectureDiagram(projectGraph) {
    let diagram = '```mermaid\ngraph TD\n';

    const nodes = projectGraph.nodes || [];
    const added = new Set();

    nodes.forEach(node => {
      const safeName = node.name.replace(/[^a-zA-Z0-9]/g, '_');
      if (!added.has(safeName)) {
        diagram += `  ${safeName}[${node.name}]\n`;
        added.add(safeName);
      }

      (node.dependencies || []).forEach(dep => {
        const safeDep = dep.replace(/[^a-zA-Z0-9]/g, '_');
        if (!added.has(safeDep)) {
          diagram += `  ${safeDep}[${dep}]\n`;
          added.add(safeDep);
        }
        diagram += `  ${safeName} --> ${safeDep}\n`;
      });
    });

    diagram += '```';
    return diagram;
  }

  // Update all docs automatically
  async updateAllDocs(files, projectName) {
    this.log('🔄 Auto-updating all documentation...');
    const results = [];

    for (const file of files) {
      const doc = await this.generateDocs(file.content, file.path, projectName);
      results.push(doc);
    }

    // Generate project README
    const readme = await this.generateProjectReadme(files, projectName);
    results.push(readme);

    this.log(`✅ Updated ${results.length} documentation files`);
    return results;
  }

  async generateProjectReadme(files, projectName) {
    let readme = `# ${projectName}\n\n`;
    readme += `## Project Structure\n\n`;
    readme += `| File | Type | Description |\n`;
    readme += `|------|------|-------------|\n`;

    files.forEach(file => {
      const type = file.path.split('.').pop();
      readme += `| ${file.path} | ${type} | Auto-generated |\n`;
    });

    readme += `\n## Getting Started\n\n`;
    readme += `1. Open index.html\n`;
    readme += `2. Start building!\n\n`;
    readme += `---\n`;
    readme += `*Generated by MAMTA AI V6 Documentation Engine*\n`;

    return { id: `readme_${Date.now()}`, filename: 'README.md', content: readme };
  }

  getStats() {
    return {
      totalDocs: this.docs.size,
      aiEnabled: !!(this.aiService && this.aiService.hasKey())
    };
  }

  async process(task, context) {
    if (task.action === 'generateDocs') {
      return await this.generateDocs(task.code, task.filename, task.projectName);
    }
    if (task.action === 'generateChangelog') {
      return await this.generateChangelog(task.changes, task.version);
    }
    if (task.action === 'generateDiagram') {
      return await this.generateArchitectureDiagram(task.projectGraph);
    }
    if (task.action === 'updateAllDocs') {
      return await this.updateAllDocs(task.files, task.projectName);
    }
    if (task.action === 'stats') {
      return this.getStats();
    }
    return { status: 'unknown_action' };
  }
}

if (typeof module !== 'undefined' && module.exports) {
  module.exports = { DocumentationEngine };
}
